package EasyGeometry;

/**
 * @author Oleg Baslak
 */

public abstract class Figure {

    /**
     * Calculates figure's perimetr (or circuit length for ellipse)
     *
     * @return calculated perimetr (or circuit length)
     */
    public abstract double GetPerimetr();

    /**
     * Calculates figure's square
     *
     * @return calculated square
     */
    public abstract double GetSquare();


}
